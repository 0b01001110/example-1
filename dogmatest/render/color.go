package render

const (
	// Red is the ANSI control code for red foreground text.
	Red = "\x1b[31m"

	// Green is the ANSI control code for green foreground text.
	Green = "\x1b[32m"

	// Yellow is the ANSI control code for yellow foreground text.
	Yellow = "\x1b[33m"

	// Cyan is the ANSI control code for cyan foreground text.
	Cyan = "\x1b[36m"

	// Reset is the ANSI control code to reset formatting.
	Reset = "\x1b[0m"
)
